function getSimpleIcon(slug: string) {
    // Check custom icons first
    if (customIcons[slug]) {
        return customIcons[slug];
    }
    const iconKey = `si${slug.charAt(0).toUpperCase()}${slug.slice(1)}` as keyof typeof simpleIcons;
    const icon = simpleIcons[iconKey];
    if (icon && 'svg' in icon) {
        return (icon as { svg: string; hex: string }).svg;
    }
    return null;
}

function TechIcon({ slug, className }: { slug: string; className?: string }) {
    const svgContent = getSimpleIcon(slug);

    if (!svgContent) {
        return <div className={`w-6 h-6 bg-[var(--foreground-muted)] rounded ${className}`} />;
    }

    return (
        <div
            className={className}
            dangerouslySetInnerHTML={{
                __html: svgContent.replace(
                    "<svg",
                    '<svg class="w-6 h-6 fill-current"'
                ),
            }}
        />
    );
}

